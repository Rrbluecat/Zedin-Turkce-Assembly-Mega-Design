void durum_cubugu(const char* islem, int yuzde) {
    int bar_genislik = 40;
    printf("%s%s: [", RENK_KALIN, islem);
    int dolu = (yuzde * bar_genislik) / 100;
    for (int i = 0; i < bar_genislik; i++) {
        if (i < dolu) printf("%s#", RENK_YESIL);
        else printf("%s-", RENK_GRI);
    }
    printf("%s] %%%d\r", RENK_SIFIR, yuzde);
    fflush(stdout);
}

